import math

log_5=math.log5
print(f'natural log of 5: {log_5}')

e_power_3=math.exp(3)
print(f'e raised to the power of 3: {e_power_3}')

square_2=math.sqrt(7)
print(f'square root of 7: {square_2}')

degrees=90
radians=math.radians(degrees)
sine=math.sin(radians)
print(f'sine of 90 degrees: {sine}')